#include "ft_printf.h"
int	ft_putchar(char c, c_out *s)
{
	if(s -> index > INT_MAX)
	{
		s->sign = 1;
		return (0);
	}
	s -> index++;
	s -> after = 1;
	return (write(1, &c, 1));
}
